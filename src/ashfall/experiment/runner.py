"""Experiment runner — orchestrates the Ashfall pipeline.

The runner coordinates five phases for each experiment:
1. Train baseline (or load existing checkpoint)
2. Generate/collect failure data
3. Run failure replay with variations
4. Fine-tune with failure curriculum (adapted condition only)
5. Evaluate all conditions

This module produces shell commands and config files that run inside
Isaac Lab's Python context. It does NOT import Isaac Lab directly —
it generates the artifacts that scripts/run_experiment.sh executes.
"""

from __future__ import annotations

import json
import logging
from dataclasses import asdict
from datetime import datetime
from pathlib import Path

import yaml

from ashfall.experiment.schema import Condition, ExperimentConfig, ExperimentResult

logger = logging.getLogger("ashfall.experiment.runner")


class ExperimentRunner:
    """Generates and manages experiment runs.

    The runner writes a run directory under ``results/<experiment_name>/<timestamp>/``
    containing:
    - ``config.yaml`` — frozen experiment config
    - ``commands.sh`` — ordered shell commands to execute in Isaac Lab context
    - ``status.json`` — run status tracker
    - ``metrics/`` — evaluation outputs
    """

    # Default Isaac Sim venv python — repaired 2026-04-28 after VSCode-snap nuked
    # the venv's symlinks. Calls into ~/Sim/IsaacLab/isaaclab.sh fall back to
    # system Python 3.10 in non-interactive shells, so we invoke the venv
    # python directly instead.
    DEFAULT_PYTHON_EXE = "/home/yusuf/Sim/isaac-sim-venv/bin/python"

    def __init__(
        self,
        ashfall_root: Path,
        phoenix_root: Path | None = None,
        python_exe: str | None = None,
    ) -> None:
        self.ashfall_root = Path(ashfall_root).resolve()
        self.results_dir = self.ashfall_root / "results"
        self.phoenix_root = (
            Path(phoenix_root).resolve()
            if phoenix_root
            else (self.ashfall_root.parent.parent / "workspace" / "go2-phoenix").resolve()
        )
        self.python_exe = python_exe or self.DEFAULT_PYTHON_EXE

    def prepare_run(self, config: ExperimentConfig) -> Path:
        """Create a run directory with all artifacts needed for execution."""
        stamp = datetime.now().strftime("%Y-%m-%d_%H-%M-%S")
        run_dir = self.results_dir / config.name / stamp
        run_dir.mkdir(parents=True, exist_ok=True)
        (run_dir / "metrics").mkdir(exist_ok=True)

        # Freeze config (round-trip through JSON so enums/Paths become primitives
        # and safe_load on the read side doesn't choke on python-object tags)
        with open(run_dir / "config.yaml", "w") as f:
            plain = json.loads(json.dumps(asdict(config), default=str))
            yaml.safe_dump(plain, f, default_flow_style=False, sort_keys=False)

        # Generate commands
        commands = self._generate_commands(config, run_dir)
        with open(run_dir / "commands.sh", "w") as f:
            f.write("#!/bin/bash\n")
            f.write("# Auto-generated by Ashfall experiment runner\n")
            f.write(f"# Experiment: {config.name}\n")
            f.write(f"# Condition: {config.condition.value}\n")
            f.write(f"# Generated: {stamp}\n\n")
            f.write("set -euo pipefail\n\n")
            for cmd in commands:
                f.write(f"echo '>>> {cmd['label']}'\n")
                f.write(cmd["command"] + "\n\n")

        # Status tracker
        status = {
            "experiment": config.name,
            "condition": config.condition.value,
            "created": stamp,
            "phases": {cmd["label"]: "pending" for cmd in commands},
            "completed": False,
        }
        with open(run_dir / "status.json", "w") as f:
            json.dump(status, f, indent=2)

        logger.info("Prepared run at %s with %d phases", run_dir, len(commands))
        return run_dir

    def _python_prefix(self) -> str:
        """Shell prefix that invokes the Isaac Sim venv python with EULA accepted.

        Phoenix and Ashfall are not pip-installed into the venv, so we set
        PYTHONPATH to their ``src`` dirs at invocation time. We also force
        ``OMNI_KIT_ACCEPT_EULA=YES`` to avoid the interactive prompt.
        """
        pythonpath = f"{self.ashfall_root / 'src'}:{self.phoenix_root / 'src'}"
        return (
            f"OMNI_KIT_ACCEPT_EULA=YES "
            f"PYTHONPATH={pythonpath} "
            f"{self.python_exe}"
        )

    def _write_adapt_override(
        self, config: ExperimentConfig, run_dir: Path
    ) -> Path:
        """Write a per-cell adaptation YAML that injects failure_fraction.

        We start from the template at ``t.adapt_config`` (relative to the
        Phoenix repo) and override:
        - ``run.name`` to ``config.name`` so checkpoints land in
          ``checkpoints/<config.name>/`` (matches the eval lookup path).
        - ``resume.path`` to the configured baseline checkpoint.
        - ``curriculum.failure_sample_fraction`` to the per-cell value.
        - ``curriculum.trajectory_dir`` to the absolute Ashfall failure dir.
        - ``env.config`` to ``config.training.env_config``.

        Without this override, every cell of a sweep would use whatever
        ``failure_sample_fraction`` the template hard-codes (typically 0.0),
        producing N identical runs.
        """
        t = config.training
        template_rel = t.adapt_config or "configs/train/adaptation.yaml"
        template_path = (self.phoenix_root / template_rel).resolve()
        if not template_path.exists():
            raise FileNotFoundError(
                f"Adaptation template not found: {template_path}"
            )
        with open(template_path) as f:
            adapt_cfg = yaml.safe_load(f)

        adapt_cfg.setdefault("run", {})["name"] = config.name
        adapt_cfg["run"]["max_iterations"] = t.max_iterations
        adapt_cfg["run"]["device"] = t.device
        adapt_cfg["run"]["seed"] = t.seed

        adapt_cfg.setdefault("resume", {})
        baseline_ckpt = config.curriculum.baseline_checkpoint or (
            "checkpoints/ashfall-baseline/latest.pt"
        )
        adapt_cfg["resume"]["path"] = baseline_ckpt

        adapt_cfg.setdefault("env", {})["config"] = t.env_config

        # Resolve failure_dir against ashfall_root if relative.
        failure_dir = Path(config.curriculum.failure_dir)
        if not failure_dir.is_absolute():
            failure_dir = (self.ashfall_root / failure_dir).resolve()

        adapt_cfg.setdefault("curriculum", {})
        adapt_cfg["curriculum"]["failure_sample_fraction"] = float(
            config.curriculum.failure_fraction
        )
        adapt_cfg["curriculum"]["trajectory_dir"] = str(failure_dir)

        # Optional per-cell mode filter. Phoenix's curriculum loader is
        # expected to honour ``failure_modes`` as a list of mode-name
        # strings; an empty list means "all modes" (the existing
        # default behaviour). Down-stream sweeps that vary the mode
        # subset (mode-subset ablation, v0.4.0) drive this field.
        if config.curriculum.failure_modes:
            adapt_cfg["curriculum"]["failure_modes"] = list(
                config.curriculum.failure_modes
            )

        # Save into Phoenix's _generated dir so the relative paths in the
        # template (e.g. variation.config) still resolve when fine_tune
        # reads it from the Phoenix cwd.
        out_dir = self.phoenix_root / "configs" / "_generated"
        out_dir.mkdir(parents=True, exist_ok=True)
        out_path = out_dir / f"adapt_{config.name}.yaml"
        with open(out_path, "w") as f:
            yaml.safe_dump(adapt_cfg, f, default_flow_style=False, sort_keys=False)

        # Mirror into the run dir for provenance.
        with open(run_dir / "adapt_override.yaml", "w") as f:
            yaml.safe_dump(adapt_cfg, f, default_flow_style=False, sort_keys=False)

        return out_path

    def _generate_commands(
        self, config: ExperimentConfig, run_dir: Path
    ) -> list[dict[str, str]]:
        commands = []
        phoenix = self.phoenix_root
        t = config.training
        e = config.evaluation
        py = self._python_prefix()

        if config.condition == Condition.BASELINE:
            # Phase 1: Train from scratch
            commands.append(
                {
                    "label": "train_baseline",
                    "command": (
                        f'cd "{phoenix}" && '
                        f"{py} -m phoenix.training.ppo_runner "
                        f"--config {t.env_config} "
                        f"--num-envs {t.num_envs} "
                        f"--max-iterations {t.max_iterations} "
                        f"--device {t.device} "
                        f"--seed {t.seed}"
                    ),
                }
            )

        elif config.condition == Condition.ADAPTED:
            # Phase 1: Fine-tune from baseline with failure curriculum.
            # Write a per-cell adapt YAML so failure_sample_fraction
            # actually reflects this cell's value (the upstream
            # phoenix.adaptation.fine_tune CLI has no flag for it).
            adapt_path = self._write_adapt_override(config, run_dir)
            try:
                adapt_rel = adapt_path.relative_to(phoenix)
            except ValueError:
                adapt_rel = adapt_path
            commands.append(
                {
                    "label": "adapt_with_failures",
                    "command": (
                        f'cd "{phoenix}" && '
                        f"{py} -m phoenix.adaptation.fine_tune "
                        f"--config {adapt_rel} "
                        f"--num-envs {t.num_envs} "
                        f"--max-iterations {t.max_iterations} "
                        f"--device {t.device}"
                    ),
                }
            )

        elif config.condition == Condition.CONTROL_RANDOM:
            # Fine-tune on random terrain without failure curriculum
            commands.append(
                {
                    "label": "finetune_random",
                    "command": (
                        f'cd "{phoenix}" && '
                        f"{py} -m phoenix.adaptation.fine_tune "
                        f"--config configs/train/adaptation.yaml "
                        f"--trajectory-dir /dev/null "
                        f"--num-envs {t.num_envs} "
                        f"--max-iterations {t.max_iterations} "
                        f"--device {t.device}"
                    ),
                }
            )

        elif config.condition == Condition.CONTROL_NOREPLAY:
            # Continue training baseline — same iters, no curriculum, same env
            commands.append(
                {
                    "label": "continue_training",
                    "command": (
                        f'cd "{phoenix}" && '
                        f"{py} -m phoenix.training.ppo_runner "
                        f"--config {t.env_config} "
                        f"--num-envs {t.num_envs} "
                        f"--max-iterations {t.max_iterations} "
                        f"--device {t.device} "
                        f"--seed {t.seed + 1}"
                    ),
                }
            )

        # Phase 2: Evaluate on each environment
        for env_name in e.eval_envs:
            env_cfg = f"configs/env/{env_name}.yaml"
            metrics_out = run_dir / "metrics" / f"metrics_{env_name}.json"
            video_flag = ""
            if e.record_video:
                video_out = run_dir / "metrics" / f"video_{env_name}.mp4"
                video_flag = f"--video-out {video_out}"

            commands.append(
                {
                    "label": f"eval_{env_name}",
                    "command": (
                        f'cd "{phoenix}" && '
                        f"{py} -m phoenix.training.evaluate "
                        f"--checkpoint checkpoints/{config.name}/latest.pt "
                        f"--env-config {env_cfg} "
                        f"--num-envs {e.num_envs} "
                        f"--num-episodes {e.num_episodes} "
                        f"--metrics-out {metrics_out} "
                        f"{video_flag} "
                        f"--device {t.device}"
                    ),
                }
            )

        return commands

    def collect_results(self, run_dir: Path) -> ExperimentResult | None:
        """Parse metrics from a completed run directory."""
        config_path = run_dir / "config.yaml"
        if not config_path.exists():
            return None

        with open(config_path) as f:
            cfg = yaml.safe_load(f)

        metrics: dict[str, dict[str, float]] = {}
        metrics_dir = run_dir / "metrics"
        for mf in sorted(metrics_dir.glob("metrics_*.json")):
            env_name = mf.stem.replace("metrics_", "")
            with open(mf) as f:
                metrics[env_name] = json.load(f)

        return ExperimentResult(
            name=cfg["name"],
            condition=cfg["condition"],
            seed=cfg["training"]["seed"],
            training_iters=cfg["training"]["max_iterations"],
            wall_time_s=0.0,  # filled by the runner after execution
            metrics=metrics,
            failure_counts={},
            checkpoint_path=str(run_dir / "checkpoint"),
        )


def load_experiment_config(path: str | Path) -> ExperimentConfig:
    """Load an experiment config from YAML."""
    with open(path) as f:
        data = yaml.safe_load(f)

    from ashfall.experiment.schema import (
        AblationAxis,
        CurriculumSpec,
        EvalSpec,
        TrainingSpec,
    )

    training = TrainingSpec(**data.get("training", {}))
    curriculum = CurriculumSpec(**data.get("curriculum", {}))
    evaluation = EvalSpec(**data.get("evaluation", {}))
    ablations = [AblationAxis(**a) for a in data.get("ablations", [])]

    return ExperimentConfig(
        name=data["name"],
        condition=Condition(data["condition"]),
        description=data.get("description", ""),
        training=training,
        curriculum=curriculum,
        evaluation=evaluation,
        ablations=ablations,
        phoenix_root=data.get("phoenix_root", "../../workspace/go2-phoenix"),
        tags=data.get("tags", []),
    )
